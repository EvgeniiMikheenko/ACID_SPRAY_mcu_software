void SetPeriod( TIM_HandleTypeDef *htim, uint32_t us, uint32_t pclk1frq ) {
	//
	//uint32_t pclk1frq = GetPCLKFrequency();
	uint32_t ciclesPerUs = us * ((pclk1frq / 1000000));
	uint16_t prescaller = (uint16_t)((ciclesPerUs + 65535) / 65536);
	if( prescaller == 0 )
		prescaller = 1;
			
	uint16_t overflow = (uint16_t)(ciclesPerUs / prescaller);
	//
	htim->Instance->PSC = (prescaller - 1);
	htim->Instance->ARR = (overflow - 1);
}


